import streamlit as st
import numpy as np
from PIL import Image
import joblib

# -------------------------
# Page Configuration
# -------------------------
st.set_page_config(
    page_title="Male vs Female Classifier",
    page_icon="👤",
    layout="centered"
)

# -------------------------
# Load Model
# -------------------------
model = joblib.load("male_female_model.pkl")

IMG_SIZE = 64

# -------------------------
# Title
# -------------------------
st.title("👤 Male vs Female Image Classifier")
st.write("Upload a face image to predict whether it is Male or Female.")

# -------------------------
# Upload Image
# -------------------------
uploaded_file = st.file_uploader(
    "Choose an Image",
    type=["jpg", "jpeg", "png"]
)

if uploaded_file is not None:

    # Read image
    image = Image.open(uploaded_file)

    # Convert image to RGB
    image = image.convert("RGB")

    # Display image
    st.image(image, caption="Uploaded Image", width=300)

    # Resize image
    resized = image.resize((IMG_SIZE, IMG_SIZE))

    # Convert to NumPy array
    resized = np.array(resized, dtype=np.float32)

    # Normalize (must match training)
    resized = resized / 255.0

    # Flatten image
    resized = resized.flatten()

    # Reshape for prediction
    resized = resized.reshape(1, -1)

    # Predict
    prediction = model.predict(resized)[0]

    # Prediction probabilities
    probability = model.predict_proba(resized)[0]

    # Display prediction
    st.subheader("Prediction")

    if prediction == 0:
        st.success("👨 Prediction: Male")
    else:
        st.success("👩 Prediction: Female")

    # Confidence
    st.subheader("Prediction Confidence")

    st.write(f"👨 Male: **{probability[0] * 100:.2f}%**")
    st.progress(float(probability[0]))

    st.write(f"👩 Female: **{probability[1] * 100:.2f}%**")
    st.progress(float(probability[1]))